function makeCurriculum(topics: string[]): Course["curriculum"] {
  return topics.map((title, i) => ({
    id: `sec-${i}`,
    title,
    lessons: Array.from({ length: 3 + (i % 3) }, (_, j) => ({
      id: `les-${i}-${j}`,
      title: `${title} — Part ${j + 1}`,
      duration_minutes: 6 + ((i + j) % 5) * 3,
      is_preview: i === 0 && j === 0,
    })),
  }));
}

function makeReviews(courseId: string, names: [string, number, string][]): Course["reviews"] {
  return names.map(([user_name, rating, comment], i) => ({
    id: `${courseId}-rev-${i}`,
    course_id: courseId,
    user_name,
    rating,
    comment,
    created_at: new Date(Date.now() - i * 1000 * 60 * 60 * 24 * 11).toISOString(),
  }));
}

