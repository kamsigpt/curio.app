export function getCourseBySlug(slug: string): Course | undefined {
  return courses.find((c) => c.slug === slug);
}

export function getCoursesByCategory(slug: string): Course[] {
  return courses.filter((c) => c.category.slug === slug);
}

export function getRelatedCourses(course: Course, limit = 4): Course[] {
  return courses.filter((c) => c.id !== course.id && c.category.id === course.category.id).slice(0, limit);
}
